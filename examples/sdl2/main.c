/******************************************************************************/
Uint32 timer(Uint32 interval, void *param)
{
	int x, y;
	int pressed;

	/* update touch readings */
	pressed = SDL_GetMouseState(&x, &y) & SDL_BUTTON(SDL_BUTTON_LEFT);
	eui_touch_update(&context, (eui_coord_t)x, (eui_coord_t)y, pressed);

	return interval;
}


/******************************************************************************/
int events(SDL_Event *event)
{
	if (event->type == SDL_WINDOWEVENT)
	{
		if (event->window.event == SDL_WINDOWEVENT_CLOSE)
		{
			return -1;
		}
	}

	return 0;
}


